func TestPoetryLockParser_Parse_Valid(t *testing.T) {
	// Create a temporary poetry.lock file with valid content
	tmpDir := t.TempDir()
	lockFile := filepath.Join(tmpDir, "poetry.lock")

	content := `# This file is automatically @generated by Poetry 1.4.2 and should not be changed by hand.

[[package]]
name = "requests"
version = "2.28.1"
description = "Python HTTP for Humans."
category = "main"
optional = false
python-versions = ">=3.7, <4"

[package.dependencies]
charset-normalizer = ">=2.0.0, <4"
idna = ">=2.5, <4"

[[package]]
name = "urllib3"
version = "1.26.15"
description = "HTTP library with thread-safe connection pooling"
category = "main"
optional = false
python-versions = ">=2.7, !=3.0.*, !=3.1.*, !=3.2.*, !=3.3.*, !=3.4.*, !=3.5.*"

[[package]]
name = "certifi"
version = "2023.5.7"
description = "Python package for providing Mozilla's CA Bundle."
category = "main"
optional = false
python-versions = ">=3.6"

[metadata]
lock-version = "2.0"
python-versions = "^3.9"
content-hash = "abc123"
`

	if err := os.WriteFile(lockFile, []byte(content), 0644); err != nil {
		t.Fatalf("Failed to create test file: %v", err)
	}

	parser := &PoetryLockParser{}
	packages, err := parser.Parse(lockFile)

	if err != nil {
		t.Fatalf("Parse failed: %v", err)
	}

	if len(packages) != 3 {
		t.Errorf("Expected 3 packages, got %d", len(packages))
	}

	// Check first package
	if packages[0].Name != "requests" {
		t.Errorf("Expected package name 'requests', got '%s'", packages[0].Name)
	}
	if packages[0].Version != "2.28.1" {
		t.Errorf("Expected version '2.28.1', got '%s'", packages[0].Version)
	}

	// Check second package
	if packages[1].Name != "urllib3" {
		t.Errorf("Expected package name 'urllib3', got '%s'", packages[1].Name)
	}
	if packages[1].Version != "1.26.15" {
		t.Errorf("Expected version '1.26.15', got '%s'", packages[1].Version)
	}

	// Check third package
	if packages[2].Name != "certifi" {
		t.Errorf("Expected package name 'certifi', got '%s'", packages[2].Name)
	}
	if packages[2].Version != "2023.5.7" {
		t.Errorf("Expected version '2023.5.7', got '%s'", packages[2].Version)
	}
}

