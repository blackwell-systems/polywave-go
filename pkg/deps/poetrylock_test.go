func TestPoetryLockParser_Parse_MalformedTOML(t *testing.T) {
	tmpDir := t.TempDir()
	lockFile := filepath.Join(tmpDir, "poetry.lock")

	// Invalid TOML: missing quotes and malformed structure
	content := `[[package]]
name = requests-without-quotes
version = 2.28.1-also-unquoted
`

	if err := os.WriteFile(lockFile, []byte(content), 0644); err != nil {
		t.Fatalf("Failed to create test file: %v", err)
	}

	parser := &PoetryLockParser{}
	packages, err := parser.Parse(lockFile)

	// Our parser is lenient and tries to extract what it can
	// But with malformed data, it should either return an error or empty packages
	if err == nil && len(packages) > 0 {
		// If it parsed something, verify it handled the malformed data
		// In this case, it might extract partial data
		if packages[0].Name == "" || packages[0].Version == "" {
			// Acceptable: extracted structure but missing data
			return
		}
	}

	// If no packages were extracted, it should return an error
	if len(packages) == 0 && err == nil {
		t.Error("Expected error for malformed TOML, got nil with no packages")
	}
}

func TestPoetryLockParser_Detect(t *testing.T) {
	parser := &PoetryLockParser{}

	tests := []struct {
		name     string
		filePath string
		expected bool
	}{
		{"Valid poetry.lock", "/path/to/poetry.lock", true},
		{"Valid with full path", "/home/user/project/poetry.lock", true},
		{"Invalid - Cargo.lock", "/path/to/Cargo.lock", false},
		{"Invalid - package-lock.json", "/path/to/package-lock.json", false},
		{"Invalid - Pipfile.lock", "/path/to/Pipfile.lock", false},
		{"Invalid - no extension", "/path/to/poetry", false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			result := parser.Detect(tt.filePath)
			if result != tt.expected {
				t.Errorf("Detect(%s) = %v, expected %v", tt.filePath, result, tt.expected)
			}
		})
	}
}

func TestPoetryLockParser_OptionalVsRequired(t *testing.T) {
	tmpDir := t.TempDir()
	lockFile := filepath.Join(tmpDir, "poetry.lock")

	content := `[[package]]
name = "required-pkg"
version = "1.0.0"
category = "main"
optional = false

[[package]]
name = "optional-pkg"
version = "2.0.0"
category = "dev"
optional = true

[[package]]
name = "another-required"
version = "3.0.0"
category = "main"
optional = false
`

	if err := os.WriteFile(lockFile, []byte(content), 0644); err != nil {
		t.Fatalf("Failed to create test file: %v", err)
	}

	parser := &PoetryLockParser{}
	packages, err := parser.Parse(lockFile)

	if err != nil {
		t.Fatalf("Parse failed: %v", err)
	}

	// Parser should return ALL packages (both optional and required)
	// per constraint: "Parse both optional and required packages (no filtering)"
	if len(packages) != 3 {
		t.Errorf("Expected 3 packages (both optional and required), got %d", len(packages))
	}

	// Verify all three packages are present
	names := make(map[string]bool)
	for _, pkg := range packages {
		names[pkg.Name] = true
	}

	expectedNames := []string{"required-pkg", "optional-pkg", "another-required"}
	for _, name := range expectedNames {
		if !names[name] {
			t.Errorf("Expected package '%s' to be parsed", name)
		}
	}
}

