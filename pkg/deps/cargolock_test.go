func TestCargoLockParser_Parse_MalformedTOML(t *testing.T) {
	// Test with file that exists but can't be read properly
	tmpDir := t.TempDir()
	cargoLock := filepath.Join(tmpDir, "Cargo.lock")

	// Create file with incomplete package section (missing required fields)
	content := `[[package]]
name = "incomplete"
# missing version and source
`

	err := os.WriteFile(cargoLock, []byte(content), 0644)
	if err != nil {
		t.Fatalf("failed to create test Cargo.lock: %v", err)
	}

	parser := &CargoLockParser{}
	res := parser.Parse(cargoLock)

	// Parser should not error on incomplete sections, but should still parse what it can
	if !res.IsSuccess() {
		t.Errorf("Parse() error = %v, want nil (parser should be lenient)", res.Errors)
	}

	packages := res.GetData()
	// Should still parse the package with just a name
	if len(packages) != 1 {
		t.Errorf("got %d packages, want 1", len(packages))
	}
	if len(packages) > 0 && packages[0].Name != "incomplete" {
		t.Errorf("packages[0].Name = %q, want %q", packages[0].Name, "incomplete")
	}
}

