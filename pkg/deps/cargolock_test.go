func TestCargoLockParser_Detect(t *testing.T) {
	tests := []struct {
		name     string
		filePath string
		want     bool
	}{
		{
			name:     "valid Cargo.lock",
			filePath: "/path/to/Cargo.lock",
			want:     true,
		},
		{
			name:     "Cargo.lock with absolute path",
			filePath: "/home/user/project/Cargo.lock",
			want:     true,
		},
		{
			name:     "not a Cargo.lock file",
			filePath: "/path/to/package.json",
			want:     false,
		},
		{
			name:     "similar but not exact",
			filePath: "/path/to/Cargo.lock.bak",
			want:     false,
		},
		{
			name:     "empty path",
			filePath: "",
			want:     false,
		},
	}

	parser := &CargoLockParser{}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := parser.Detect(tt.filePath)
			if got != tt.want {
				t.Errorf("Detect(%q) = %v, want %v", tt.filePath, got, tt.want)
			}
		})
	}
}

func TestCargoLockParser_MultiplePackages(t *testing.T) {
	// Test handling multiple packages with various field orders
	tmpDir := t.TempDir()
	cargoLock := filepath.Join(tmpDir, "Cargo.lock")

	content := `version = 3

[[package]]
name = "aho-corasick"
version = "0.7.18"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package]]
name = "anyhow"
version = "1.0.56"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package]]
name = "async-trait"
version = "0.1.52"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package]]
name = "cfg-if"
version = "1.0.0"
source = "registry+https://github.com/rust-lang/crates.io-index"
`

	err := os.WriteFile(cargoLock, []byte(content), 0644)
	if err != nil {
		t.Fatalf("failed to create test Cargo.lock: %v", err)
	}

	parser := &CargoLockParser{}
	res := parser.Parse(cargoLock)

	if !res.IsSuccess() {
		t.Fatalf("Parse() error = %v, want nil", res.Errors)
	}

	packages := res.GetData()
	if len(packages) !=4 {
		t.Fatalf("got %d packages, want 4", len(packages))
	}

	// Verify all packages are parsed correctly
	expectedPackages := []struct {
		name    string
		version string
	}{
		{"aho-corasick", "0.7.18"},
		{"anyhow", "1.0.56"},
		{"async-trait", "0.1.52"},
		{"cfg-if", "1.0.0"},
	}

	for i, expected := range expectedPackages {
		if packages[i].Name != expected.name {
			t.Errorf("packages[%d].Name = %q, want %q", i, packages[i].Name, expected.name)
		}
		if packages[i].Version != expected.version {
			t.Errorf("packages[%d].Version = %q, want %q", i, packages[i].Version, expected.version)
		}
		if packages[i].Source != "registry+https://github.com/rust-lang/crates.io-index" {
			t.Errorf("packages[%d].Source = %q, want registry URL", i, packages[i].Source)
		}
	}
}

func TestUnquoteTOML(t *testing.T) {
	tests := []struct {
		name  string
		input string
		want  string
	}{
		{
			name:  "double quotes",
			input: `"value"`,
			want:  "value",
		},
		{
			name:  "single quotes",
			input: `'value'`,
			want:  "value",
		},
		{
			name:  "no quotes",
			input: "value",
			want:  "value",
		},
		{
			name:  "with whitespace",
			input: `  "value"  `,
			want:  "value",
		},
		{
			name:  "empty string",
			input: `""`,
			want:  "",
		},
		{
			name:  "single character",
			input: `"a"`,
			want:  "a",
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := unquoteTOML(tt.input)
			if got != tt.want {
				t.Errorf("unquoteTOML(%q) = %q, want %q", tt.input, got, tt.want)
			}
		})
	}
}
