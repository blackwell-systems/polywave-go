func TestCargoLockParser_Parse_Valid(t *testing.T) {
	// Create temporary Cargo.lock file with valid content
	tmpDir := t.TempDir()
	cargoLock := filepath.Join(tmpDir, "Cargo.lock")

	content := `# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 3

[[package]]
name = "serde"
version = "1.0.136"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "13bd41f6f4502f53f53d55e5e6e7eaa9c3b4e5b9e3f9d6b9f3b9c4f3b9c4f3b9"

[[package]]
name = "serde_json"
version = "1.0.79"
source = "registry+https://github.com/rust-lang/crates.io-index"
checksum = "8e8d9fa5c3b3d9f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3f3"
`

	err := os.WriteFile(cargoLock, []byte(content), 0644)
	if err != nil {
		t.Fatalf("failed to create test Cargo.lock: %v", err)
	}

	parser := &CargoLockParser{}
	res := parser.Parse(cargoLock)

	if !res.IsSuccess() {
		t.Fatalf("Parse() error = %v, want nil", res.Errors)
	}

	packages := res.GetData()
	if len(packages) != 2 {
		t.Fatalf("got %d packages, want 2", len(packages))
	}

	// Verify first package
	if packages[0].Name != "serde" {
		t.Errorf("packages[0].Name = %q, want %q", packages[0].Name, "serde")
	}
	if packages[0].Version != "1.0.136" {
		t.Errorf("packages[0].Version = %q, want %q", packages[0].Version, "1.0.136")
	}
	if packages[0].Source != "registry+https://github.com/rust-lang/crates.io-index" {
		t.Errorf("packages[0].Source = %q, want %q", packages[0].Source, "registry+https://github.com/rust-lang/crates.io-index")
	}

	// Verify second package
	if packages[1].Name != "serde_json" {
		t.Errorf("packages[1].Name = %q, want %q", packages[1].Name, "serde_json")
	}
	if packages[1].Version != "1.0.79" {
		t.Errorf("packages[1].Version = %q, want %q", packages[1].Version, "1.0.79")
	}
}

